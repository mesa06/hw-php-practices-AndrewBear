<!--D. 水仙花數 = 三位數各數字的立方和等於該數(例如:153)，請寫程式列出所有的水仙花數。-->

<?php

$x = array();

for ($i = 1; $i <= 9; $i++){
    for ($j = 0; $j <= 9; $j++){
        for ($k = 0; $k <= 9; $k++){
            if ($i * $i * $i + $j * $j * $j + $k * $k * $k
            == 100 * $i + 10 * $j + $k){
                $x[] = 100 * $i + 10 * $j + $k; 
            }
        }
    }
}

print_r ($x);

?>